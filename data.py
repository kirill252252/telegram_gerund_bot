def get_accepted_translations(verb: str) -> list[str]:
    """Возвращает все допустимые варианты перевода глагола в нижнем регистре."""
    if verb in VERB_TRANSLATIONS:
        return [t.lower() for t in VERB_TRANSLATIONS[verb]]
